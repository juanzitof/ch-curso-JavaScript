var nombreDeUsuario = prompt("Ingrese su nombre");
  console.log(`ingreso el nombre: ${nombreDeUsuario}`)

var apellidoDeUsuario = prompt("Ingresu su apellido");
  console.log(`su apellido es:${apellidoDeUsuario}`)

var nombreCompleto = nombreDeUsuario + " " + apellidoDeUsuario;
  console.log(`su nombre completo es:${nombreCompleto}`)

var edadDeUsuario = parseInt (prompt ("Cual es tu edad?"));
  console.log(`ingreso la edad de: ${edadDeUsuario}`)
 
alert(`su nombre completo es: ${nombreCompleto} y su edad es ${edadDeUsuario}`)
